void LCD_PutString(unsigned short x, unsigned short y, unsigned char *s, unsigned int fColor, unsigned int bColor) {
	unsigned char l=0;
	while(*s) {
		if( *s < 0x80) {
			LCD_PutChar(x+l*8,y,*s,fColor,bColor);
			s++;l++;
		}
		else{
			PutGB1616(x+l*8,y,(unsigned char*)s,fColor,bColor);
			s+=2;l+=2;
		}
	}
}


