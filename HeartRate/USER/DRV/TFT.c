void LCD_SetPos(unsigned int x0,unsigned int x1,unsigned int y0,unsigned int y1)
{
	Write_Cmd_Data(0x46, x1<<8|x0);   //一次性设定x1,x2(高)
    Write_Cmd_Data(0x47, y1); //y2
    Write_Cmd_Data(0x48, y0);  //y1
    Write_Cmd_Data(0x20, x0);  //设定RAM开始写的地址
    Write_Cmd_Data(0x21, y0);
    Write_Cmd(0x22); //设定好写寄存器,开始写
}

void  Write_Cmd_Data(uint cmd,uint dat)  
{
	Write_Cmd(cmd);
	Write_Data(dat);
}

void ClearScreen(unsigned int bColor)
{
	uint i,j;
	LCD_SetPos(0,239,0,319);
	for (i=0;i<320;i++)
	{
		for(j=0;j<240;j++ )
		{
			Write_Data(bColor);
		}
	}
} 

void Fill_Rectangle(uint x,uint y,uint x1,uint y1,uint color)
{
	uint i,j;
	LCD_SetPos(x,x1,y,y1);
	for(i=y;i<y1;i++)
	{
		for(j=x;j<x1;j++)
		{
			Write_Data(color);
		}
	}
}


void Put_pixel(unsigned int x,unsigned int y,unsigned int color)
{
	LCD_SetPos(x,x,y,y);
	Write_Data(color);
}

#include "8x16.h"

void LCD_PutChar8x16(unsigned short x, unsigned short y, char c, unsigned int fColor, unsigned int bColor)
{
	uint i,j;
	LCD_SetPos(x,x+7,y,y+15);
	for (i=0;i<16;i++)
	{
		uchar m=Font8x16[c*16+i];
		for (j=0;j<8;j++)
		{
			if((m&0x80)==0x80)
			{
				Write_Data(fColor);
			}
			else 
			{	
				Write_Data(bColor);
			}
			m<<=1;
		}			
	}
}

#include "GB1616.h"
void PutGB1616(unsigned short x, unsigned short  y, unsigned char c[2], unsigned int fColor,unsigned int bColor)
{
	uint i,j,k;
	LCD_SetPos(x,x+15,y,y+15);
	for(i=0;i<HANZIGESHU;i++)
	{
		if(codeGB_16[i].Index[0]==c[0]&&codeGB_16[i].Index[1]==c[1])
		{
			for(j=0;j<32;j++)
			{
				uchar m=codeGB_16[i].Msk[j];
				for(k=0;k<8;k++)
				{
					if((m&0x80)==0x80)
					{
						Write_Data(fColor);
				 	}
					else
					{
						Write_Data(bColor);
					}
					m<<=1;
				}
			}
		}
	}
}

void Line(unsigned int X0,unsigned int Y0,unsigned int X1,unsigned int Y1,unsigned int color)
{
	int dx = X1 - X0;
	int dy = Y1 - Y0;
	//int P  = 2 * dy - dx;
	//int dobDy = 2 * dy;
	//int dobD = 2 * (dy - dx);
	int PointX = 0,PointY = 0;
	int incx = 0,incy = 0;
	int distance = 0,xerr = 0,yerr = 0;
	unsigned int i = 0;
	if(dx == 0)
	{
		PointX = X0;
		if(Y0 < Y1)
		{
			PointY = Y0;
		}
		else{
			PointY = Y1;
		}
		for(i = 0;i <= ((Y0<Y1) ? (Y1-Y0) : (Y0-Y1));i++)
		{
			Put_pixel(PointX,PointY,color);
			PointY++;
		}
		return;
	}
	if(dy == 0)	
	{
		PointY = Y0;
		if(X0 < X1)
		{
			PointX = X0;
		}
		else{
			PointX = X1;
		}
		for(i = 0;i <= ((X0<X1) ? (X1-X0) : (X0-X1));i++)
		{
			Put_pixel(PointX,PointY,color);
			PointX++;
		}
		return;
	}

	if(dx > 0)
		incx = 1;
	else if(dx == 0)
		incx = 0;
	else
		incx = -1;

	if(dy > 0)
		incy = 1;
	else if(dy == 0)
		incy = 0;
	else
		incy = -1;

    dx = ((X0>X1) ? (X0-X1) : (X1-X0));
    dy = ((Y0>Y1) ? (Y0-Y1) : (Y1-Y0));

    if(dx>dy) distance=dx;
    else distance=dy;

    PointX = X0;
    PointY = Y0;
    for(i=0;i<=distance+1;i++){
    	Put_pixel(PointX,PointY,color);
        xerr+=dx;
        yerr+=dy;
        if(xerr>distance){
            xerr-=distance;
            PointX+=incx;
        }
        if(yerr>distance){
            yerr-=distance;
            PointY+=incy;
        }
    }
}


void Rectangle(unsigned int left,unsigned int top,unsigned int right,unsigned int bottom,unsigned int color){
	Line(left,top,right,top,color);
	Line(left,top,left,bottom,color);
	Line(right,top,right,bottom,color);
	Line(left,bottom,right,bottom,color);
}

